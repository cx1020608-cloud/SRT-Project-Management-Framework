let LANG = localStorage.getItem('pm_lang') || 'zh';

function t(key) {
    return (I18N[LANG] && I18N[LANG][key]) || key;
}

function setLang(lang) {
    LANG = lang;
    localStorage.setItem('pm_lang', lang);
    document.documentElement.lang = lang === 'ja' ? 'ja' : 'zh';
    // Update static DOM elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.getElementById('langLabel').textContent = lang === 'zh' ? '日本語' : '中文';
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
    document.getElementById('langToggle').addEventListener('click', () => {
        setLang(LANG === 'zh' ? 'ja' : 'zh');
        if (typeof renderView === 'function') renderView();
    });
    setLang(LANG);
});
